    public void clearForm() {
        this.txtName.setText("");
        this.txtPrice.setText("");
        this.rdoTax.setSelected(true);
    }

    public boolean checkForm() {
        boolean a = true;
        

        String name = this.txtName.getText();
        String price = this.txtPrice.getText();
        if (name.equals("")) {
            JOptionPane.showMessageDialog(this, "không được để trống name!");
            a = false;
        }
        if (price.equals("")) {
            JOptionPane.showMessageDialog(this, "không được để trống Price!");
            a = false;
        }
        if (price.equals("") == false){
            try {
            Double.parseDouble(this.txtPrice.getText());
        } catch (NumberFormatException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Giá không hợp lệ");
            a = false;
        }
        }

        return a;
    }

