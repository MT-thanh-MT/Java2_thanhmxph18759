    private void tblSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSanPhamMouseClicked
        int index = this.tblSanPham.getSelectedRow();
        if (index == -1) {
            return;
        }

        Product sp = (Product) this.ql.getList().get(index);
        this.txtName.setText(sp.getName());

        this.txtPrice.setText(Double.toString(sp.getPrice()));
        if (sp.getImportTax() == 0) {
            this.rdoNoTax.setSelected(true);
        } else {
            this.rdoTax.setSelected(true);
        }


    }//GEN-LAST:event_tblSanPhamMouseClicked

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed

        int viTri = this.tblSanPham.getSelectedRow();
        if (viTri == -1) {
            JOptionPane.showMessageDialog(this, "không có bản ghi nào được chọn");
            return;
        }
        int xacNhan = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa bản ghi không?");

        if (xacNhan == JOptionPane.YES_OPTION) {

            this.ql.delete(viTri);
            this.hienThiTable();
            JOptionPane.showMessageDialog(this, "Delete successfully");
            clearForm();
        } else if (xacNhan == JOptionPane.NO_OPTION) {
            return;
        } else if (xacNhan == JOptionPane.CANCEL_OPTION) {
            return;
        }


    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int viTri = this.tblSanPham.getSelectedRow();
        if (viTri == -1) {
            JOptionPane.showMessageDialog(this, "không có bản ghi nào được chọn");
            return;
        }
        
        if (checkForm() == false) {
            return;
        }

        String name = this.txtName.getText();
        double price = Double.parseDouble(this.txtPrice.getText());
        if (this.rdoTax.isSelected() == true) {
            Product sp = new Product(name, price);
            this.ql.update(viTri, sp);
        } else {
            NoTaxProduct sp = new NoTaxProduct(name, price);
            this.ql.update(viTri, sp);
        }

        this.hienThiTable();
        JOptionPane.showMessageDialog(this, "Update successfully");
    }//GEN-LAST:event_btnUpdateActionPerformed

