    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        int index = this.tblStaff.getSelectedRow();
        if (index == -1) {
            this.lblStaffIdEnror.setText("không có bản ghi nào được chọn");
            return;
        }

        int xacNhan = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa bản ghi không?");

        if (xacNhan == JOptionPane.YES_OPTION) {

            this.ql.Remove(index);
            this.hienThiTable();
            JOptionPane.showMessageDialog(this, "Remove thành công");
            clearForm();
        } else if (xacNhan == JOptionPane.NO_OPTION) {
            return;
        } else if (xacNhan == JOptionPane.CANCEL_OPTION) {
            return;
        }

    }//GEN-LAST:event_btnRemoveActionPerformed

