    public Boolean checkForm() {
        String id = this.txtStaffId.getText();
        String name = this.txtFullname.getText();

        Boolean a = true;
        if (id.equals("")) {
            this.lblStaffIdEnror.setText("Không để trống ID!");
            a = false;
        } else {
            this.lblStaffIdEnror.setText("");
        }
        if (name.equals("")) {
            this.lblFullnameEnror.setText("Không để trống Name!");
            a = false;
        } else {
            this.lblFullnameEnror.setText("");
        }
        return a;
    }

    public void clearForm() {
        this.txtStaffId.setText("");
        this.txtFullname.setText("");
        this.rdoMale.setSelected(true);
        this.txaNotes.setText("");
        this.lblFullnameEnror.setText("");
        this.lblStaffIdEnror.setText("");
    }

