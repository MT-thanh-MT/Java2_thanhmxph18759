    public Boolean checkForm() {
        String id = this.txtStaffId.getText();
        String name = this.txtFullname.getText();

        Boolean a = true;
        if (id.equals("")) {
            this.lblStaffIdError.setText("Không để trống ID!");
            a = false;
        } else {
            this.lblStaffIdError.setText("");
        }
        if (name.equals("")) {
            this.lblFullnameError.setText("Không để trống Name!");
            a = false;
        } else {
            this.lblFullnameError.setText("");
        }
        return a;
    }
    
    public void clearForm() {
        this.txtStaffId.setText("");
        this.txtFullname.setText("");
        this.rdoMale.setSelected(true);
        this.txaNotes.setText("");
        this.lblFullnameError.setText("");
        this.lblStaffIdError.setText("");
    }
