    private void hienThiTable() {
        DefaultTableModel dtm = (DefaultTableModel) this.tblStaff.getModel();

        dtm.setRowCount(0);

        ArrayList<Management> ds = this.ql.getList();
        for (int i = 0; i < ds.size(); i++) {
            Management nv = (Management) ds.get(i);

            Object[] rowData = new Object[]{
                nv.getId(),
                nv.getName(),
                nv.getCountry(),
                nv.getGender(),
                nv.getStatus()
            };

            dtm.addRow(rowData);
        }
    }
