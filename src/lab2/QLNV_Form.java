    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
         checkForm();
        boolean a = checkForm();
        if (a == false) {
            return;
        }
        String id = this.txtStaffId.getText();
        String name = this.txtFullname.getText();
        String country = (String) this.cbbCountry.getSelectedItem();
        String gender;
        if (this.rdoMale.isSelected() == true) {
            gender = "Male";
        } else {
            gender = "Female";
        }

        String status;
        if (this.chkSingle.isSelected() == true) {
            status = "Single";
        } else {
            status = "Married";
        }

        Management nv = new Management(id, name, country, gender, status);
        this.ql.Add(nv);
        this.hienThiTable();
        JOptionPane.showMessageDialog(this, "Thêm mới thành công");
        clearForm();
    }//GEN-LAST:event_btnCreateActionPerformed

    private void tblStaffMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblStaffMouseClicked
        int index = this.tblStaff.getSelectedRow();
        if (index == -1) {
            return;
        }
        Management nv = (Management) this.ql.getList().get(index);
        this.txtStaffId.setText(nv.getId());
        this.txtFullname.setText(nv.getName());
        this.cbbCountry.setSelectedItem(nv.getCountry());

        if (nv.getGender().equals("Male")) {
            this.rdoMale.setSelected(true);
        } else {
            this.rdoFemale.setSelected(true);
        }

        if (nv.getStatus().equals("Married")) {
            this.chkSingle.setSelected(false);
        } else {
            this.chkSingle.setSelected(true);
        }

    }//GEN-LAST:event_tblStaffMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int index = this.tblStaff.getSelectedRow();

        if (index == -1) {
            this.lblStaffIdError.setText("không có bản ghi nào được chọn!");
            return;
        }
        checkForm();
        boolean a = checkForm();
        if (a == false) {
            return;
        }

        String id = this.txtStaffId.getText();
        String name = this.txtFullname.getText();
        String country = (String) this.cbbCountry.getSelectedItem();
        String gender;
        if (this.rdoMale.isSelected() == true) {
            gender = "Male";
        } else {
            gender = "Female";
        }

        String status;
        if (this.chkSingle.isSelected() == true) {
            status = "Single";
        } else {
            status = "Married";
        }

        Management nv = new Management(id, name, country, gender, status);
        this.ql.Update(index, nv);
        this.hienThiTable();
        JOptionPane.showMessageDialog(this, "Update thành công");
        clearForm();
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
         int index = this.tblStaff.getSelectedRow();
        if (index == -1) {
            this.lblStaffIdError.setText("không có bản ghi nào được chọn");
            return;
        }

        int xacNhan = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa bản ghi không?");

        if (xacNhan == JOptionPane.YES_OPTION) {

            this.ql.Remove(index);
            this.hienThiTable();
            JOptionPane.showMessageDialog(this, "Remove thành công");
            clearForm();
        } else if (xacNhan == JOptionPane.NO_OPTION) {
            return;
        } else if (xacNhan == JOptionPane.CANCEL_OPTION) {
            return;
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

